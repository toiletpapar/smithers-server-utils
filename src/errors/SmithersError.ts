class SmithersError extends Error {
  public isSmithersError = true
  public type: SmithersErrorTypes
  public context: any
  
  constructor(type: SmithersErrorTypes, message: string, context?: any) {
    super(message)
    this.type = type
    this.context = context
  }
}

