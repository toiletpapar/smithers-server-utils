  MANGA_UPDATE_UNKNOWN_APP_KEY = "SMITHERS_MANGA_UPDATE_UNKNOWN_APP_KEY",
  MANGA_UPDATE_UPDATE_AT_LEAST_ONE_PROPERTY = "SMITHERS_MANGA_UPDATE_UPDATE_AT_LEAST_ONE_PROPERTY",

  MANGA_CRAWL_TARGET_NOT_FOUND = "SMITHERS_MANGA_CRAWL_TARGET_NOT_FOUND",
